package Elements;

public class Ownership {
	
	private String id;
	private String c_code;
	private String used;
	
	public String getId() {
		return id;
	}
	public String getC_code() {
		return c_code;
	}
	public String getUsed() {
		return used;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setC_code(String c_code) {
		this.c_code = c_code;
	}
	public void setUsed(String used) {
		this.used = used;
	}
}
